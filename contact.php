<script>

 $(document).ready(function(){

  $('#sample_form').on('submit', function(event){
   event.preventDefault();
   var count_error = 0;

   if($('#name').val() == '')
   {
    $('#name_error').text('Nama Lengkap Harap Diisi');
    count_error++;
   }
   else
   {
    $('#name_error').text('');
   }

   if($('#nomortelepon').val() == '')
   {
    $('#nomortelepon_error').text('Nomor Telepon Harap Diisi');
    count_error++;
   }
   else
   {
    $('#nomortelepon_error').text('');
   }

   if($('#idline').val() == '')
   {
    $('#idline_error').text('ID Line Harap Diisi');
    count_error++;
   }
   else
   {
    $('#idline_error').text('');
   }

   if($('#idline').val() == '')
   {
    $('#idline_error').text('ID Line Harap Diisi');
    count_error++;
   }
   else
   {
    $('#idline_error').text('');
   }

   if($('#universitas').val() == '')
   {
    $('#universitas_error').text('Universitas Harap Diisi');
    count_error++;
   }
   else
   {
    $('#universitas_error').text('');
   }

   if($('#angkatan').val() == '')
   {
    $('#angkatan_error').text('Angkatan Harap Diisi');
    count_error++;
   }
   else
   {
    $('#angkatan_error').text('');
   }

   if($('#jurusan').val() == '')
   {
    $('#jurusan_error').text('Jurusan Harap Diisi');
    count_error++;
   }
   else
   {
    $('#jurusan_error').text('');
   }

   if($('#skill').val() == '')
   {
    $('#skill_error').text('Mohon Diisi Detail Kemampuan Anda Secara Rinci');
    count_error++;
   }
   else
   {
    $('#skill_error').text('');
   }

   if($('#reason').val() == '')
   {
    $('#reason_error').text('Alasan Mengikuti PKM Harap Diisi');
    count_error++;
   }
   else
   {
    $('#reason_error').text('');
   }

   if(count_error == 0)
   {
    $.ajax({
     url:"process.php",
     method:"POST",
     data:$(this).serialize(),
     beforeSend:function()
     {
      $('#save').attr('disabled', 'disabled');
      $('#process').css('display', 'block');
     },
     success:function(data)
     {
      var percentage = 0;

      var timer = setInterval(function(){
       percentage = percentage + 20;
       progress_bar_process(percentage, timer);
      }, 1000);
     }
    })
   }
   else
   {
    return false;
   }
  });

  function progress_bar_process(percentage, timer)
  {
   $('.progress-bar').css('width', percentage + '%');
   if(percentage > 100)
   {
    clearInterval(timer);
    $('#sample_form')[0].reset();
    $('#process').css('display', 'none');
    $('.progress-bar').css('width', '0%');
    $('#save').attr('disabled', false);
    $('#success_message').html("<div class='alert alert-success'>Terima kasih telah mendaftar ke SCC. Data Anda telah dikirim dan diterima oleh kami. Kami akan segera menghubungi Anda melalui WhatsApp/Line. Dimohon untuk menunggu kabar dari kami. Terima kasih!</div>");
    setTimeout(function(){
     $('#success_message').html('');
   }, 50000);
   }
  }

 });
</script>
